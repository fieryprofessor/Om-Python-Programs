import math
a = int(input("Enter a: "))
b = int(input("Enter b: "))

print("Sum:", a + b)
print("Difference:", a - b)
print("Product:", a * b)
print("Quotient:", a / b)
print("Remainder:", a % b)
print("log10(a):", math.log10(a))
print("a^b:", a ** b)

